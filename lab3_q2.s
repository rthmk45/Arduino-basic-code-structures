Reset_Handler
		MOV R0, #0
		LDR R1, =NUM
		MOV R2, #10
		
LOOP
	LDR R3, [R1], #4
	ADD R0, R0, R3
	SUBS R2, R2, #1
	BNE LOOP
	
	LDR R1, =RES
	STR R0, [R1]
STOP
	B STOP
	
