	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R1,=RESULT1
	LDR R7,=OUTPUT
	LDR R2,[R1]
	AND R3,R2,#0X00FF
	AND R4,R2,#0XFF00
	LSR R4,R4,#8
	ADD R7,R4,R3
STOP
	B STOP
RESULT1 DCD 0X0908
	AREA MYDATA,DATA,READWRITE
OUTPUT DCD 0
	END